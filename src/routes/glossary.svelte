        <h4 class="section-title mb-0">ART GLOSSARY</h4>
        <div class="spacer spacer-line border-primary ml-0">&nbsp;</div>
        <div class="spacer">&nbsp;</div>
        <dl>

          <dt>Ala Prima</dt>
          <dd>wet on wet. A technique of layering wet paint on wet paint</dd>

          <dt>Angle</dt>
          <dd>two lines that intersect or join form an angle</dd>

          <dt>Chiaroscuro</dt>
          <dd>use of strong contrasts of light and dark</dd>

          <dt>Colour</dt>
          <dd>the property possessed by a paint which produces different sensations on the eye as a result of the way it
            reflects or emits light</dd>

          <dt>Contour/Outline</dt>
          <dd>the visual outer shape of an object</dd>

          <dt>Cool</dt>
          <dd>colours that indicate a low temperature such as white, blue</dd>

          <dt>Drawing</dt>
          <dd>recording elements from nature whilst preserving their relationships of points, lines, and angles.
            Observing, interpreting, intending, action of moving pencil on a medium, resulting in a reproduction of
            what one observes on the medium</dd>

          <dt>Edges</dt>
          <dd>contrast of values. Not a line</dd>

          <dt>Gesture</dt>
          <dd>a suggestion of movement</dd>

          <dt>Light</dt>
          <dd>strong, sharp, diffused</dd>

          <dt>Line</dt>
          <dd>a mark between 2 points on a flat surface</dd>

          <dt>Opaic (opacity)</dt>
          <dd>the quality of not letting light through the medium</dd>

          <dt>Perspective</dt>
          <dd>a point of view</dd>

          <dt>Point</dt>
          <dd>a dot or mark (without dimension) on a drawing surface</dd>

          <dt>Shape</dt>
          <dd>the form of something</dd>


          <dt>Tableau</dt>
          <dd>a group of figures representing a scene from a story or from history</dd>

          <dt>Transparancy</dt>
          <dd>the quality of letting light through the medium</dd>

          <dt>Value</dt>
          <dd>relative lightness/darkness</dd>

          <dt>Warm</dt>
          <dd>colours that indicate a hot temperature such as strong yellow, orange, red</dd>

        </dl>
